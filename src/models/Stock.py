class Stock:
    def __init__(open: float, close: float, high: float, low: float, average_volume: float, dividend_yield: float, type: string, last: float, symbol: string, prevclose: float):
        this.open = open
        this.close = close
        this.high = high
        this.low = low
        this.average_volume = average_volume
        this.dividend_yield = dividend_yield
        this.type = type
        this.last = last
        this.symbol = symbol
        this.prevclose = prevclose
